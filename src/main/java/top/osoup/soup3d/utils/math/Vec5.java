package top.osoup.soup3d.utils.math;

public class Vec5 {
    public float x;
    public float y;
    public float z;
    public float u;
    public float v;

    public Vec5(
        float x,
        float y,
        float z,
        float u,
        float v
    ) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.u = u;
        this.v = v;
    }
}
